    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        int remove = tb_sach.getSelectedRow();
        if (remove == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn 1 dòng để xóa");
        } else {
            int opt = JOptionPane.showConfirmDialog(null, "Ban co muon xoa", "xoa", JOptionPane.YES_NO_OPTION);
            if (opt == 0) {
                int row = tb_sach.getSelectedRow();
                listSach.remove(row);
                addRow();
                JOptionPane.showMessageDialog(this, "Xóa thành công");
            }
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        List<Sach> list = getList();
        Sach sach = new Sach();
        String tenSach = txt_tensach.getText();
        String tacGia = txt_tacgia.getText();
        int namIn;
        namIn = Integer.parseInt((String) cb_namin.getSelectedItem());
        if (rd_truyen.isSelected()) {
            sach.setTheLoai(1);
        } else if (rd_tieu.isSelected()) {
            sach.setTheLoai(2);
        }

        sach.setTenSach(tenSach);
        sach.setTacGia(tacGia);
        sach.setNamIn(namIn);

        listSach.add(sach);
        addRow();
    }//GEN-LAST:event_btn_themActionPerformed

    private void tb_sachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_sachMouseClicked
        // TODO add your handling code here:
        int row = tb_sach.getSelectedRow();
        txt_tensach.setText((String) tb_sach.getValueAt(row, 0));
        txt_tacgia.setText((String) tb_sach.getValueAt(row, 1));
        cb_namin.setSelectedItem(tb_sach.getValueAt(row, 3).toString());
        String theLoai = tb_sach.getValueAt(row, 2).toString();
        if (tb_sach.getValueAt(row, 2).toString().equals("Tiểu thuyết")) {
            rd_tieu.setSelected(true);
        } else {
            rd_truyen.setSelected(true);
        }
    }//GEN-LAST:event_tb_sachMouseClicked

