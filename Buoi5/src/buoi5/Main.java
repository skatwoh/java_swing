    public void addRow() {
        List<Sach> list = getList();
        defaultTableModel = (DefaultTableModel) tb_sach.getModel();
        defaultTableModel.setRowCount(0);
        for (Sach sach : list) {
            defaultTableModel.addRow(
                    new Object[]{
                        sach.getTenSach(), sach.getTacGia(), getTenTheLoai(sach.getTheLoai()), sach.getNamIn()
                    }
            );
        }
    }
    
    public String getTenTheLoai(int theLoai) {
        if (theLoai == 1) {
            return "Truyện ngắn";
        } else if (theLoai == 2) {
            return "Tiểu thuyết";
        } else {
            return null;
        }
    }

