    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        String str = txt_sophong.getText();
        String str1 = txt_diachi.getText();
        String str2 = txt_nguoithue.getText();
        if ("".equals(str)) {
            JOptionPane.showMessageDialog(this, "Không được để trống số phòng");
        } else if ("".equals(str1)) {
            JOptionPane.showMessageDialog(this, "Không được để trống địa chỉ");
        } else if ("".equals(str2)) {
            JOptionPane.showMessageDialog(this, "Không được để trống người thuê");
        } else {

            List<Phong> list = getList();
            Phong phong = new Phong();
            String soPhong = txt_sophong.getText();
            String diaChi = txt_diachi.getText();
            String nguoiThue = txt_nguoithue.getText();

            int soNguoi;
            soNguoi = Integer.parseInt((String) cb_songuoi.getSelectedItem());
            if (rd_chuathue.isSelected()) {
                phong.setTinhTrang(1);
            } else {
                phong.setTinhTrang(0);
            }

            phong.setSoNguoi(soNguoi);
            phong.setDiaChi(diaChi);
            phong.setNguoiThue(nguoiThue);
            phong.setSoPhong(soPhong);

            
            list.add(phong);
            addRow();
        }
    }//GEN-LAST:event_btn_themActionPerformed

    private void tb_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_tableMouseClicked
        // TODO add your handling code here:
        int row = tb_table.getSelectedRow();
        txt_sophong.setText((String) tb_table.getValueAt(row, 0).toString());
        txt_diachi.setText((String) tb_table.getValueAt(row, 1).toString());
        cb_songuoi.setSelectedItem(tb_table.getValueAt(row, 4).toString());
        txt_nguoithue.setText((String) tb_table.getValueAt(row, 2).toString());
        String trangThai = tb_table.getValueAt(row, 3).toString();
        if (tb_table.getValueAt(row, 3).toString().equals("Chưa thuê")) {
            rd_chuathue.setSelected(true);
        } else {
            rd_dathue.setSelected(true);
        }
        
    }//GEN-LAST:event_tb_tableMouseClicked

