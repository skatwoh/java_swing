    public void addRow() {
        List<Phong> list = getList();
        defaultTableModel = (DefaultTableModel) tb_table.getModel();
        defaultTableModel.setRowCount(0);
        for (Phong phong : list) {
            defaultTableModel.addRow(
                    new Object[]{
                        phong.getSoPhong(),
                        phong.getDiaChi(),
                        phong.getNguoiThue(),
                        getTheTinhTrang(phong.getTinhTrang()),
                        phong.getSoNguoi()
                    }
            );
        }
    }

    public void addItemComboBox() {
        ArrayList<String> listItem = new ArrayList<>();
        listItem.add("1");
        listItem.add("2");
        listItem.add("3");
        listItem.add("4");

        defaultComboBoxModel = (DefaultComboBoxModel) cb_songuoi.getModel();

        for (String item : listItem) {
            defaultComboBoxModel.addElement(item);
        }

        cb_songuoi.setSelectedIndex(0);
    }

    public void addItemColumns() {
        ArrayList<String> listItems = new ArrayList<>();
        listItems.add("Số Phòng");
        listItems.add("Địa Chỉ");
        listItems.add("Người Thuê");
        listItems.add("Tình Trạng");
        listItems.add("Số Người");

        defaultTableModel = (DefaultTableModel) tb_table.getModel();
        defaultTableModel.setColumnCount(0);
        for (String listItem : listItems) {
            defaultTableModel.addColumn(listItem);
        }
    }

    public String getTheTinhTrang(int tinhTrang) {
        if (tinhTrang == 1) {
            return "Chưa thuê";
        } else if (tinhTrang == 0) {
            return "Thuê";
        } else {
            return "null";
        }
    }

