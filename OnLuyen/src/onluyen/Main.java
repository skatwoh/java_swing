    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        String str = txt_masv.getText();
        String str1 = txt_tensv.getText();
        if("".equals(str)){
            JOptionPane.showMessageDialog(this, "Không được để trống mã sv");
        } else if("".equals(str1)){
            JOptionPane.showMessageDialog(this, "Không được để trống tên sv");
        } else {
            List<SinhVien> list = getList();
            SinhVien sinhVien = new SinhVien();
            String maSV = txt_masv.getText();
            String tenSV = txt_tensv.getText();
            Object boMon = cb_bomon.getSelectedItem();
            
            sinhVien.setMaSV(maSV);
            sinhVien.setTenSV(tenSV);
            sinhVien.setBoMon((String) boMon);
            sinhVien.setTrangThai("Đang hoạt động");
            
            list.add(sinhVien);
            addRow();
            
            JOptionPane.showMessageDialog(this, "Thêm thành công");
        }
    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        int remove = tb_sinhvien.getSelectedRow();
        if(remove == -1){
            JOptionPane.showMessageDialog(this, "Hãy chọn 1 dòng để xóa");
        } else {
            int opt = JOptionPane.showConfirmDialog(null,"Bạn có muốn xóa ?","xóa",JOptionPane.YES_NO_CANCEL_OPTION);
            if(opt==0){
                int row = tb_sinhvien.getSelectedRow();
                listSinhVien.remove(row);
                addRow();
                JOptionPane.showMessageDialog(this, "Xóa thành công");
            }
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

    private void tb_sinhvienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_sinhvienMouseClicked
        // TODO add your handling code here:
        int row = tb_sinhvien.getSelectedRow();
        txt_masv.setText(tb_sinhvien.getValueAt(row, 0).toString());
        txt_tensv.setText(tb_sinhvien.getValueAt(row, 1).toString());
        cb_bomon.setSelectedItem(tb_sinhvien.getValueAt(row, 2));
    }//GEN-LAST:event_tb_sinhvienMouseClicked

