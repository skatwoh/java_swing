    public void addItemComboBox() {
        ArrayList<String> listItem = new ArrayList<>();
        listItem.add("UDPM");
        listItem.add("WEB");

        defaultComboBoxModel = (DefaultComboBoxModel) cb_bomon.getModel();
        for (String item : listItem) {
            defaultComboBoxModel.addElement(item);
        }

        cb_bomon.setSelectedIndex(0);
    }

    public void addItemColumns() {
        ArrayList<String> listItems = new ArrayList<>();
        listItems.add("Mã sinh viên");
        listItems.add("Tên sinh viên");
        listItems.add("Bộ môn");
        listItems.add("Trạng thái");

        defaultTableModel = (DefaultTableModel) tb_sinhvien.getModel();
        defaultTableModel.setColumnCount(0);
        for (String listItem : listItems) {
            defaultTableModel.addColumn(listItem);
        }
    }

    public void addRow() {
        List<SinhVien> list = getList();
        defaultTableModel = (DefaultTableModel) tb_sinhvien.getModel();
        defaultTableModel.setRowCount(0);
        for (SinhVien sinhVien : list) {
            defaultTableModel.addRow(
                    new Object[]{
                        sinhVien.getMaSV(),
                        sinhVien.getTenSV(),
                        sinhVien.getBoMon(),
                        sinhVien.getTrangThai()
                    }
            );
        }
    }
    

