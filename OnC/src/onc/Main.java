    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        if (txt_tenkh.getText().equals("") || txt_soluong.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Không được để trống");
        } else if (Integer.parseInt(txt_soluong.getText()) < 0) {
            JOptionPane.showMessageDialog(this, "nhập số lượng lỗi");
        } else {
            HoaDon hd = new HoaDon();
            String tenKH = txt_tenkh.getText();
            Object loaiVe = cb_loaive.getSelectedItem();
            int soLuong = Integer.parseInt(txt_soluong.getText());
            int thanhTien;
            if (cb_loaive.getSelectedItem().equals("Trẻ em")) {
                thanhTien = soLuong * 80000;
            } else {
                thanhTien = soLuong * 100000;
            }

            hd.setTenKH(tenKH);
            hd.setLoaiVe((String) loaiVe);
            hd.setSoLuong(soLuong);
            hd.setThanhTien(thanhTien);
            listHoaDon.add(hd);

            addRow();
            JOptionPane.showMessageDialog(this, "thêm thành công");
        }
    }//GEN-LAST:event_btn_themActionPerformed

