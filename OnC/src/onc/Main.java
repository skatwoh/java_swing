    public void addRow() {
        defaultTableModel = (DefaultTableModel) tb_hoadon.getModel();
        defaultTableModel.setRowCount(0);

        for (HoaDon x : listHoaDon) {
            defaultTableModel.addRow(
                    new Object[]{
                        x.getTenKH(),
                        x.getLoaiVe(),
                        x.getSoLuong(),
                        x.getThanhTien()
                    }
            );
        }
    }

    public void ghiFile() throws FileNotFoundException, IOException {
        File file = new File("khanh.txt");
        if (!file.exists()) {
            try {
                file.createNewFile();
            } catch (IOException ex) {
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        FileOutputStream fos = new FileOutputStream(file);
        ObjectOutputStream oos = new ObjectOutputStream(fos);
        for (HoaDon hoaDon : listHoaDon) {
            oos.writeObject(hoaDon);
        }
        oos.close();
        fos.close();
    }

    public ArrayList<HoaDon> moFile() throws FileNotFoundException, IOException, ClassNotFoundException {
        File file = new File("khanh.txt");
        if (!file.exists()) {
            try {
                file.createNewFile();
            } catch (IOException ex) {
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        FileInputStream fis = new FileInputStream(file);
        ObjectInputStream ois = new ObjectInputStream(fis);

        HoaDon hoaDon = new HoaDon();
        ArrayList<HoaDon> list = new ArrayList<>();
        while (fis.available() > 0) {
            hoaDon = (HoaDon) ois.readObject();
            list.add(hoaDon);
        }
        System.out.println(list.size());

        ois.close();
        fis.close();

        return list;
    }

    public HoaDon timTheoTen(String ten) {
        for (HoaDon hoaDon : listHoaDon) {
            if (hoaDon.getTenKH().equalsIgnoreCase(ten)) {
                return hoaDon;
            }
        }
        return null;
    }

