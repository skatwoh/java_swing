    private void addItemColumns() {
        List<String> listItems = new ArrayList<>();
        listItems.add("Tên sinh viên");
        listItems.add("Giới tính");
        listItems.add("Tuổi");
        listItems.add("Địa chỉ");
        dtm = (DefaultTableModel) tb_sinhvien.getModel();
        dtm.setColumnCount(0);
        for (String listItem : listItems) {
            dtm.addColumn(listItem);
        }
    }

    private void addRow() {
        List<SinhVien> list = getList();
        dtm = (DefaultTableModel) tb_sinhvien.getModel();
        dtm.setRowCount(0);
        for (SinhVien x : list) {
            dtm.addRow(
                    new Object[] {
                            x.getHoTen(),
                            x.getGioiTinh(),
                            x.getNam(),
                            x.getDiaChi()
                    });
        }
    }

    private void ghiFile() throws IOException {
        File file = new File("file.txt");
        if (!file.exists()) {
            try {
                file.createNewFile();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

        FileOutputStream fos = new FileOutputStream(file);
        ObjectOutputStream oos = new ObjectOutputStream(fos);
        for (SinhVien x : listSinhVien) {
            oos.writeObject(x);
        }

        oos.close();
        fos.close();
    }

