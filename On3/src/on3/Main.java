    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        if (txt_hoten.getText().equals("") || txt_diachi.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Không được để trống.");
        } else {
            List<SinhVien> list = getList();
            SinhVien sv = new SinhVien();
            String hoTen = txt_hoten.getText();
            String diaChi = txt_diachi.getText();

            if (rd_nam.isSelected()) {
                sv.setGioiTinh("Nam");
            } else {
                sv.setGioiTinh("Nữ");
            }
            int namSinh = Integer.parseInt(cb_namsinh.getSelectedItem().toString());

            sv.setHoTen(hoTen);
            sv.setNam(2022 - namSinh);
            sv.setDiaChi(diaChi);

            listSinhVien.add(sv);

            JOptionPane.showMessageDialog(this, "Thêm thành công.");

        }
    }// GEN-LAST:event_btn_themActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        int remove = tb_sinhvien.getSelectedRow();
        if (remove == -1) {
            JOptionPane.showMessageDialog(this, "Chọn 1 dòng để xóa.");
        } else {
            int opt = JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa", "yes", JOptionPane.YES_NO_OPTION);
            if (opt == 0) {
                listSinhVien.remove(opt);
            }
        }
    }// GEN-LAST:event_btn_xoaActionPerformed

    private void btn_loadActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btn_loadActionPerformed
        // TODO add your handling code here:
        addRow();
    }// GEN-LAST:event_btn_loadActionPerformed

    private void btn_ghiActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btn_ghiActionPerformed
        // TODO add your handling code here:
        SinhVien sv = new SinhVien();
        sv.setHoTen(txt_hoten.getText());
        sv.setDiaChi(txt_diachi.getText());
        if (rd_nam.isSelected()) {
            sv.setGioiTinh("Nam");
        } else {
            sv.setGioiTinh("Nữ");
        }
        int namSinh = Integer.parseInt(cb_namsinh.getSelectedItem().toString());
        cb_namsinh.setSelectedItem(namSinh);
        listSinhVien.add(sv);
        try {
            ghiFile();
        } catch (IOException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }
    }// GEN-LAST:event_btn_ghiActionPerformed

    private void tb_sinhvienMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_tb_sinhvienMouseClicked
        // TODO add your handling code here:
        int row = tb_sinhvien.getSelectedRow();
        txt_hoten.setText(tb_sinhvien.getValueAt(row, 0).toString());
        txt_diachi.setText(tb_sinhvien.getValueAt(row, 3).toString());
        cb_namsinh.setSelectedItem(tb_sinhvien.getValueAt(row, 2));
        if (tb_sinhvien.getValueAt(row, 1).toString().equals("Nam")) {
            rd_nam.setSelected(true);
        } else {
            rd_nu.setSelected(true);
        }

    }// GEN-LAST:event_tb_sinhvienMouseClicked

