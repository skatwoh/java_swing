    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        if (txt_tensv.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "No title");
        } else {
            SinhVien sv = new SinhVien();
            String tenSV = txt_tensv.getText();
            Object boMon = cb_mon.getSelectedItem().toString();

            sv.setTen(tenSV);
            sv.setMon((String) boMon);
            sv.setTrangThai("Active");

            sinhVienIService.add(sv);
            addRow();
            JOptionPane.showMessageDialog(this, "Them thanh cong");

        }
    }// GEN-LAST:event_btn_themActionPerformed

