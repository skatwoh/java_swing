    private void addComboBox() {
        ArrayList<String> listItem = new ArrayList<>();
        listItem.add("UDPM");
        listItem.add("WEB");

        dcbm = (DefaultComboBoxModel) cb_mon.getModel();
        for (String item : listItem) {
            dcbm.addElement(item);
        }
    }

    private void addColumns() {
        ArrayList<String> listItems = new ArrayList<>();
        listItems.add("Tên sinh viên");
        listItems.add("Môn");
        listItems.add("Trạng thái");

        dtm = (DefaultTableModel) tb_sinhvien.getModel();
        dtm.setColumnCount(0);
        for (String items : listItems) {
            dtm.addColumn(items);
        }
    }

    private void addRow() {
        dtm = (DefaultTableModel) tb_sinhvien.getModel();
        dtm.setRowCount(0);
        for (SinhVien x : sinhVienIService.getList()) {
            dtm.addRow(
                    new Object[]{
                        x.getTen(),
                        x.getMon(),
                        x.getTrangThai()
                    });
        }

    }

