    private void ghiFile() throws FileNotFoundException, IOException {
        File file = new File("dataObject.txt");
        if (!file.exists()) {
            try {
                file.createNewFile();
            } catch (IOException ex) {
            }
        }

        FileOutputStream fos = new FileOutputStream(file);
        ObjectOutputStream oos = new ObjectOutputStream(fos);
        for (PhieuMuon phieuMuon : listPhieuMuon) {
            oos.writeObject(phieuMuon);
        }

        oos.close();
        fos.close();
    }

    private ArrayList<PhieuMuon> docFile() throws FileNotFoundException, IOException, ClassNotFoundException {
        File file = new File("dataObject.txt");
        if (!file.exists()) {
            try {
                file.createNewFile();
            } catch (IOException ex) {

            }
        }
        FileInputStream fis = new FileInputStream(file);
        ObjectInputStream ois = new ObjectInputStream(fis);

        PhieuMuon phieuMuon = new PhieuMuon();
        ArrayList<PhieuMuon> listPhieuMuon = new ArrayList<>();
        while (fis.available() > 0) {
            phieuMuon = (PhieuMuon) ois.readObject();
            listPhieuMuon.add(phieuMuon);

        }
        System.out.println(listPhieuMuon.size());

        ois.close();
        fis.close();

        return listPhieuMuon;

    }

