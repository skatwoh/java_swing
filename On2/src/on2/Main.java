    private void tb_sinhvienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_sinhvienMouseClicked
        // TODO add your handling code here:
        int row = tb_sinhvien.getSelectedRow();
        txt_tensv.setText(tb_sinhvien.getValueAt(row, 0).toString());
        txt_tensach.setText(tb_sinhvien.getValueAt(row, 3).toString());
        cb_bomon.setSelectedItem(tb_sinhvien.getValueAt(row, 1));
        Object gioiTinh = tb_sinhvien.getValueAt(row, 2);
        if (tb_sinhvien.getValueAt(row, 2).toString().equals("Nam")) {
            rd_nam.setSelected(true);
        } else {
            rd_nu.setSelected(true);
        }
    }//GEN-LAST:event_tb_sinhvienMouseClicked

    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        String str = txt_tensv.getText();
        String str1 = txt_tensach.getText();
        if (str.equals("") || str1.equals("")) {
            JOptionPane.showMessageDialog(this, "Không được để trống.");
        } else {
            List<PhieuMuon> list = getList();
            PhieuMuon phieuMuon = new PhieuMuon();
            String tenSV = txt_tensv.getText();
            String tenSach = txt_tensach.getText();
            Object boMon = cb_bomon.getSelectedItem();
            if (rd_nam.isSelected()) {
                phieuMuon.setGioiTinh("Nam");
            } else {
                phieuMuon.setGioiTinh("Nữ");
            }

            phieuMuon.setTenSV(tenSV);
            phieuMuon.setTenSach(tenSach);
            phieuMuon.setBoMon((String) boMon);

            listPhieuMuon.add(phieuMuon);

            JOptionPane.showMessageDialog(this, "Thêm thành công.");
        }

    }//GEN-LAST:event_btn_themActionPerformed

